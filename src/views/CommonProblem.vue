              <h4>什么是众筹？</h4>
                众筹来源于英语单词Crowdfunding，即群众筹资。

                任何人都可以成为众筹发起者，通过互联网发布自己的创意，获得更多人的关注，从而募集生产创造所需的资金，将创意变成现实。而每一位支持者可以根据支持金额的不同，获得相应的回报。当然，前提是合法合规。



                在众筹网，众筹还是一套基于兴趣圈层的用户运营体系，这意味着：发起者可以找到自己的核心用户，实现从0到1的积累，或者从1到10的破圈。基于众筹独特的业务模式，完成用户培育、运营到扩增的链条，实现从品牌增量到产品销售，流量运营到整合营销的完整闭环。




                一次众筹是如何运行的？
                在一次众筹中，发起众筹的个人、团体或者企业（自然人、法人或其他组织）被称为发起者，而其他参与众筹的众筹网用户被称为支持者。

                发起者会提出一个创意想法，根据自己的能力谨慎地设置『众筹计划』、需要达到的『目标金额』、『项目回报』和『众筹时限』，并且利用『视频、图片和文案』等力所能及的方式阐述自己创意的起源和实现方式。

                支持者浏览不同的项目，如果发现了自己心仪的创意，就可以选择参与众筹。参与众筹最主要的方式是给予发起人一定的资金支持，并在众筹完全结束后获得创造者许诺的回报。如果能够为发起人提出一些中肯的建议，也是一种非常有价值的参与形式。

                如果众筹目标金额达成，项目就被视为成功。如果众筹时限到期，且众筹目标金额未达成，项目就被视为失败，所有募得款项将由众筹网系统退还给支持者。

                当然，某些情况下发起者的创意想法并不完善，或者距离将想法落地实现为一个产品仍有一段距离，这时发起者可能会选择先进行『创意』。发起者通过创意收集意见并且累积核心用户，待条件成熟后，再将创意项目转成为众筹项目。支持者可以『看好』创意项目，可以参与项目讨论，关注项目以期获得项目的最近消息和进展更新——总体来说和众筹并无太大区别。但是创意项目并不涉及筹集资金。




                创意/众筹有什么作用？
                展示创意

                众筹网为有才华的人提供一个展示创意的空间。只要愿意，每个人都可以与大众分享想法与灵感，获得反馈与支持。


                找到同好

                基于共同的兴趣，人们会自发地聚集，用创意想法吸引到志趣相投的同好，这恰好是众筹模式的核心之一。同时这也有助于创作者建立起属于自己的小社群。


                募集资金

                对于任何创造来说，资金问题都是必须要解决的。通过一次众筹，创造者可以在产品完成之前就募集一部分生产所需的资金，缓解项目初期的压力。


                市场调查

                通过一次众筹，创意生产者可以了解自己的产品被多少人喜爱和认可，并根据获得的信息进行策略的调整。


                宣传推广

                是的，众筹可以在短期内集中帮您的项目进行宣传和推广。随着众筹的进行，会有越来越多的人了解您的创意和想法。如果人们认可您和您的项目，他们会很乐意成为您的“布道者”。


                获得反馈

                通过与众筹的支持这交流，创造者们可以充分了解自己的项目有哪些突出的优点被认可，也可以了解有哪些不足之处需要尽快完善。这对一个起步阶段的项目十分重要。



                众筹网的众筹规则？
                众筹网目前只支持All or Nothing的众筹模式。这意味着创作者只有在设定的众筹目标金额达成之后才能拿到募集的资金。如果众筹目标没有达成，募集的资金将全部退回给支持者。

                众筹与其他平台的预售有区别吗？

                简单来说，预售更像是单纯的买卖行为，而众筹具备更强的社区属性。

                对于一次预售来说，销售金额和订单数是更重要的指标。而对于一次众筹来说，有多少支持者认可您的项目，并给出有价值的意见和反馈则更重要。

                此外，众筹是all or nothing模式，预售则不然。众筹未达成目标则发起者无法获得筹资金额、预售则是无论预售多少，都按实际销售数量计算。




                众筹是非法集资吗？
                众筹网上的项目不会提供股权或者资产收益作为给支持者的回报，一切的资金都是为了完成一个具体的文化创意项目——无论是虚拟回报（游戏激活码、影片拷贝、观看一次演出等）还是实体回报（手工艺品、书籍、模型等）。

                并且，众筹网对于众筹项目发起人有相应的资质审查，在资金拨付方面也有内部、外部的风控机制，不存在非法集资的风险。




                众筹网如何保障资金安全？
                作为平台，我们会尽可能保障大家的资金安全。为了做到这一点，众筹网不会在众筹成功后一次性将所有筹集资金支付给众筹发起人，而是随着项目完成的进度逐步发放。

                同时，众筹网依据新加坡相关法律法规，使用第三方金融监管平台对于资金进行监管，以确保每个发起者、商家、支持者的相关权益不受到损害。




